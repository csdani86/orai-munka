# Operátrok

print(5 % 2) # maradék képzés
print(2 ** 5)#hatványozás (pow függvény is jó)

''' Logikai operátorok
and - és 
or - vagy
not - tagadás
'''

szam = 5
print (szam > 3) 
print (szam > 3 or szam > 5) 

''' vagy - or művelet
|   A   |   B   |  A or B  |
-----------------------------
| False | False |  False   |
|  True | False |  True    |
| False | True  |  True    |
| True  | True  |  True    |

'''
''' és - and művelet
|   A   |   B   |  A and B |
-----------------------------
| False | False |  False   |
|  True | False |  False   |
| False | True  |  False   |
| True  | True  |  True    |
'''

''' tagadás - NOT
|   A    |  Not A  |
--------------------
|  False |  True   |
|  True  |  False  |
'''

